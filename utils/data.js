function getFoods(state) {
  const { foodMode, selectedCategories, excludedFoods, myMenu } = state;
  const mode = FOOD_MODES[foodMode] || FOOD_MODES.shengji;
  const result = [];
  const seen = new Set();

  // 1. 如果 selectedCategories 包含 '我的菜单'，先加入 myMenu 中的菜（去重）
  if (selectedCategories && selectedCategories.includes('我的菜单')) {
    for (const name of myMenu || []) {
      if (!seen.has(name)) {
        seen.add(name);
        result.push(name);
      }
    }
  }

  // 2. 遍历 selectedCategories 中的真实分类，从对应 mode 的 categories 中取 items，排除 excludedFoods
  const catMap = new Map(mode.categories.map(c => [c.name, c.items]));
  for (const catName of selectedCategories || []) {
    if (catName === '我的菜单') continue;
    const items = catMap.get(catName);
    if (!items) continue;
    for (const name of items) {
      if ((excludedFoods || []).includes(name)) continue;
      if (!seen.has(name)) {
        seen.add(name);
        result.push(name);
      }
    }
  }

  return result;
}

function getFoodsWithSource(state) {
  const { foodMode, selectedCategories, excludedFoods, myMenu } = state;
  const mode = FOOD_MODES[foodMode] || FOOD_MODES.shengji;
  const result = [];
  const seen = new Set();

  if (selectedCategories && selectedCategories.includes('我的菜单')) {
    for (const name of myMenu || []) {
      if (!seen.has(name)) {
        seen.add(name);
        result.push({ name, source: '我的菜单' });
      }
    }
  }

  const catMap = new Map(mode.categories.map(c => [c.name, c.items]));
  for (const catName of selectedCategories || []) {
    if (catName === '我的菜单') continue;
    const items = catMap.get(catName);
    if (!items) continue;
    for (const name of items) {
      if ((excludedFoods || []).includes(name)) continue;
      if (!seen.has(name)) {
        seen.add(name);
        result.push({ name, source: catName });
      }
    }
  }

  return result;
}

function getAllFoods(state) {
  const { foodMode, excludedFoods, myMenu } = state;
  const mode = FOOD_MODES[foodMode] || FOOD_MODES.shengji;
  const result = [];
  const seen = new Set();
  const excluded = new Set(excludedFoods || []);

  for (const name of myMenu || []) {
    if (excluded.has(name)) continue;
    if (!seen.has(name)) {
      seen.add(name);
      result.push(name);
    }
  }

  for (const cat of mode.categories || []) {
    for (const name of cat.items || []) {
      if (excluded.has(name)) continue;
      if (!seen.has(name)) {
        seen.add(name);
        result.push(name);
      }
    }
  }

  return result;
}

function getTips(state) {
  const { foodMode, customTips } = state;
  const mode = FOOD_MODES[foodMode] || FOOD_MODES.shengji;
  return Object.assign({}, mode.tips, customTips || {});
}

